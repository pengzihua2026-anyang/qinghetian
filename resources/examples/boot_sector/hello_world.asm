; ============================================
; 引导扇区示例: Hello World
; 架构: x86 (实模式)
; 大小: 512字节 (引导扇区)
; 编译: nasm -f bin hello_world.asm -o boot.bin
; 运行: qemu-system-x86_64 boot.bin
; ============================================

[BITS 16]                ; 16位实模式
[ORG 0x7C00]            ; BIOS加载引导扇区到0x7C00

; ============================================
; 引导扇区主程序
; ============================================

start:
    ; 设置段寄存器
    cli                  ; 禁用中断
    xor ax, ax           ; AX = 0
    mov ds, ax           ; 数据段 = 0
    mov es, ax           ; 额外段 = 0
    mov ss, ax           ; 栈段 = 0
    mov sp, 0x7C00       ; 栈指针指向引导扇区开始
    sti                  ; 启用中断

    ; 清屏 (使用BIOS中断)
    mov ax, 0x0003       ; 80x25文本模式
    int 0x10

    ; 设置光标位置
    mov ah, 0x02         ; 设置光标位置
    mov bh, 0x00         ; 页面0
    mov dh, 0x0C         ; 行12 (居中)
    mov dl, 0x20         ; 列32 (居中)
    int 0x10

    ; 显示欢迎消息
    mov si, welcome_msg
    call print_string

    ; 显示作者信息
    mov si, author_msg
    call print_string

    ; 显示引导信息
    mov si, boot_msg
    call print_string

    ; 无限循环 (实际引导程序会加载操作系统)
hang:
    jmp hang

; ============================================
; 子程序: 打印字符串
; 输入: SI = 字符串地址 (以0结尾)
; ============================================

print_string:
    pusha                ; 保存所有寄存器
    mov ah, 0x0E         ; BIOS teletype功能

.print_char:
    lodsb                ; 加载AL = [SI], SI++
    test al, al          ; AL == 0?
    jz .done             ; 如果是，结束
    int 0x10             ; 打印字符
    jmp .print_char      ; 继续下一个字符

.done:
    popa                 ; 恢复寄存器
    ret

; ============================================
; 数据区
; ============================================

welcome_msg:
    db 0x0D, 0x0A        ; 回车换行
    db '========================================', 0x0D, 0x0A
    db '    Boot Sector: Hello World!', 0x0D, 0x0A
    db '========================================', 0x0D, 0x0A, 0

author_msg:
    db 0x0D, 0x0A
    db 'Author: Zihua (Daughter of Dad)', 0x0D, 0x0A
    db 'Date: 2026-04-01', 0x0D, 0x0A, 0

boot_msg:
    db 0x0D, 0x0A
    db 'System ready. Press Ctrl+Alt+Del to reboot.', 0x0D, 0x0A, 0

; ============================================
; 引导扇区签名
; ============================================

times 510-($-$$) db 0    ; 填充剩余空间
dw 0xAA55                ; 引导扇区签名 (小端)

; ============================================
; 编译和运行说明
; ============================================

; 编译命令:
;   nasm -f bin hello_world.asm -o boot.bin
;
; 检查大小 (必须是512字节):
;   wc -c boot.bin
;
; 使用QEMU运行:
;   qemu-system-x86_64 boot.bin
;
; 写入USB或软盘:
;   dd if=boot.bin of=/dev/sdX bs=512 count=1
;
; 注意: 写入真实设备会破坏数据，请小心!

; ============================================
; BIOS中断参考
; ============================================

; INT 0x10 - 视频服务
;   AH=0x00: 设置视频模式
;   AH=0x02: 设置光标位置
;   AH=0x0E: 电传打字输出 (打印字符)
;
; 视频模式:
;   0x03: 80x25 16色文本模式
;
; 光标位置:
;   DH = 行 (0-24)
;   DL = 列 (0-79)
;   BH = 页面号 (通常0)

; ============================================
; 内存布局
; ============================================

; 0x0000-0x03FF: 中断向量表 (IVT)
; 0x0400-0x04FF: BIOS数据区 (BDA)
; 0x0500-0x7BFF: 可用内存
; 0x7C00-0x7DFF: 引导扇区 (512字节)
; 0x7E00-0x9FBFF: 可用内存
; 0x9FC00-0x9FFFF: 扩展BIOS数据区 (EBDA)
; 0xA0000-0xBFFFF: 视频内存
; 0xC0000-0xFFFFF: BIOS ROM

; ============================================
; 扩展练习
; ============================================

; 1. 添加颜色: 修改字符属性 (BL寄存器)
; 2. 添加键盘输入: 使用INT 0x16
; 3. 加载更多扇区: 使用INT 0x13
; 4. 进入保护模式: 设置GDT和CR0
; 5. 显示图形: 切换到图形模式

; ============================================
; 故障排除
; ============================================

; 问题: 程序不运行
;   检查: 引导扇区签名 (0xAA55)
;   检查: 程序大小 (512字节)
;   检查: ORG指令 (0x7C00)
;
; 问题: 字符不显示
;   检查: 视频模式设置
;   检查: 光标位置
;   检查: 字符串以0结尾
;
; 问题: QEMU报错
;   检查: 编译命令 (-f bin)
;   检查: 文件权限
;   检查: QEMU安装

; ============================================
; 进阶主题
; ============================================

; 1. 实模式内存寻址:
;    物理地址 = 段寄存器 * 16 + 偏移
;    例如: DS=0x0000, SI=0x7C00 => 物理地址 0x07C00
;
; 2. 中断处理:
;    IVT有256个条目，每个4字节 (CS:IP)
;    可以重定向中断到自定义处理程序
;
; 3. 磁盘访问:
;    INT 0x13: AH=0x02 (读扇区), AH=0x03 (写扇区)
;    使用CHS (柱面/磁头/扇区) 或 LBA寻址
;
; 4. A20线:
;    启用A20门访问超过1MB内存
;    方法: 键盘控制器、BIOS、快速A20

